package Lista1;

public class Aviao {
	
	private String fabricante = "Boeing";
	private String modelo = "737";
	private int numeroDePassageiro = 162;
	
	
	public String getFabricante() {
		return fabricante;
	}
	public void setFabricante(String fabricante) {
		this.fabricante = fabricante;
	}
	public String getModelo() {
		return modelo;
	}
	public void setModelo(String modelo) {
		this.modelo = modelo;
	}
	public int getNumeroDePassageiros() {
		return numeroDePassageiro;
	}
	public void setNumeroDePassageiros(int numeroDePassageiro) {
		this.numeroDePassageiro = numeroDePassageiro;
	}
}
